    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jComboPais.getSelectedItem().toString() == "Seleccione") {
            JOptionPane.showMessageDialog(null, "Selecciona un Pais!!!", "Ventana Error Datos", JOptionPane.ERROR_MESSAGE);
            return;
        }

        MostrarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboPaisActionPerformed
        String pais = jComboPais.getSelectedItem().toString();

        switch (pais) {
            case "Bolivia":
                datosp = new DPBolivia();
                nombre = datosp.CrearNombre(jTextnombre.getText(), jTextapellidop.getText(), jTextapellidoM.getText());
                fecha = datosp.CrearFecha(Integer.parseInt(jComboDia.getSelectedItem().toString()), jComboMes.getSelectedItem().toString(), Integer.parseInt(jComboAño.getSelectedItem().toString()));

                break;
            case "USA":
                datosp = new DPUSA();
                nombre = datosp.CrearNombre(jTextnombre.getText(), jTextapellidop.getText(), jTextapellidoM.getText());
                fecha = datosp.CrearFecha(Integer.parseInt(jComboDia.getSelectedItem().toString()), jComboMes.getSelectedItem().toString(), Integer.parseInt(jComboAño.getSelectedItem().toString()));

                break;
            case "Japon":
                datosp = new DPJapon();
                nombre = datosp.CrearNombre(jTextnombre.getText(), jTextapellidop.getText(), jTextapellidoM.getText());
                fecha = datosp.CrearFecha(Integer.parseInt(jComboDia.getSelectedItem().toString()), jComboMes.getSelectedItem().toString(), Integer.parseInt(jComboAño.getSelectedItem().toString()));

                break;
            default:
        }

    }//GEN-LAST:event_jComboPaisActionPerformed

    private void MostrarTabla() {
        Object data[] = new Object[3];

        //Cargamos Tabla      
        DefaultTableModel modeloDeLaTabla = (DefaultTableModel) jTable1.getModel();

        data[0] = jComboPais.getSelectedItem().toString();
        data[1] = nombre.MostrarDato();
        data[2] = fecha.MostrarDato();

        modeloDeLaTabla.addRow(data);
    }

